
public class Day01 {

	public static void main(String[] args) {
		int a = 0; //선언문 변수 이름이 a 초기값은 0으로 설정
		int b = 0; //선언문 변수 이름이 b 초기값은 0으로 설정
		
		a = 30; //30을 a변수 값에 대입
		b = 50; //50을 b변수 값에 대입
		
		int c = 0; //선언문 변수 이름이 c 초기값은 0으로 설정
		c = a + b; //변수 a와 변수 b를 더한 값을 c에 대입
		System.out.println(c); // c변수 값을 콘솔에 출력
		//System.out.println() 명령어는 콘솔에 출력하라
		// 실행 방법은 ctrl키를 누른 상태에서 f11번 키 누름
		
	}

}
